package udemycourseL;

public class worsksheet5point6 {
    public static void main(String[] args) {
        for (int i = 0; i <= 20; i++) {
            if (i % 2 == 0) {
                System.out.println("-----------------------------------------------");
                System.out.println(i + " is Even");
            }
            else {
                System.out.println(i + " is Odd");
                System.out.println("-----------------------------------------------");
            }
            System.out.println("-----------------------------------------------");
        }
    }
}
